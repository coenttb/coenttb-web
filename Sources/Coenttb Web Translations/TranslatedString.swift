extension TranslatedString {
    public init(_ dateComponents: DateComponents) {
        // Convert the provided components into total minutes
        let totalMinutes = (dateComponents.day ?? 0) * 24 * 60
                        + (dateComponents.hour ?? 0) * 60
                        + (dateComponents.minute ?? 0)
        
        // Convert total minutes to hours and minutes
        let hours = totalMinutes / 60
        let minutes = totalMinutes % 60
        
        // Generate the English and Dutch strings, conditionally including hours
        var english = ""
        var dutch = ""
        
        if hours > 0 {
            english += "\(hours) hour\(hours == 1 ? "" : "s")"
            dutch += "\(hours) uur"
        }
        
        if minutes > 0 {
            if !english.isEmpty {
                english += " "
            }
            if !dutch.isEmpty {
                dutch += " "
            }
            english += "\(minutes) minute\(minutes == 1 ? "" : "s")"
            dutch += "\(minutes) minu\(minutes == 1 ? "ut" : "ten")"
        }
        
        self = .init(dutch: dutch, english: english)
    }
}
